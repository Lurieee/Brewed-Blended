    private void loadDatabaseTables(){
        try { 
                Class.forName("com.mysql.cj.jdbc.Driver");
                String url = "jdbc:MySQL://localhost:3306/brewedblended_db";
                String user = "root";
                String pass = "";
                Connection con = DriverManager.getConnection(url,user,pass);
                Statement st = con.createStatement();

                // Clear the Products table
                DefaultTableModel productsModel = (DefaultTableModel)tblProducts.getModel();
                productsModel.setRowCount(0);
                // Get data from Products database
                String query = "SELECT * FROM products";
                ResultSet rs = st.executeQuery(query);
                while(rs.next()){
                    String productId = String.valueOf(rs.getInt("ProductId"));
                    String productName = String.valueOf(rs.getString("ProductName"));
                    String price = String.valueOf(rs.getDouble("Price"));
                    String inStock = String.valueOf(rs.getInt("InStock"));
                    
                    String tbData[] = {productId,productName,price,inStock};
                    productsModel.addRow(tbData); 
                }
                
                // Clear the Orders table
                DefaultTableModel ordersModel = (DefaultTableModel)tblOrders.getModel();
                ordersModel.setRowCount(0);
                // Get data from Orders database
                query = "SELECT * FROM orders";
                rs = st.executeQuery(query);
                while(rs.next()){
                    String orderId = String.valueOf(rs.getInt("OrderId"));
                    String userId = String.valueOf(rs.getInt("UserId"));
                    String productId = String.valueOf(rs.getInt("ProductId"));
                    String productQty = String.valueOf(rs.getInt("Quantity"));
                    String totalAmount = String.valueOf(rs.getDouble("TotalAmount"));
                    String orderDate = String.valueOf(rs.getString("OrderDate"));
                    
                    String tbData[] = {orderId,userId,productId,productQty,totalAmount,orderDate};
                    ordersModel = (DefaultTableModel)tblOrders.getModel();
                    ordersModel.addRow(tbData); 
                }
                
                // Clear the Users table
                DefaultTableModel usersModel = (DefaultTableModel)tblUsers.getModel();
                usersModel.setRowCount(0);
                // Get data from Users database
                query = "SELECT * FROM users";
                rs = st.executeQuery(query);
                while(rs.next()){
                    String userId = String.valueOf(rs.getInt("UserId"));
                    String firstName = String.valueOf(rs.getString("FirstName"));
                    String lastName = String.valueOf(rs.getString("LastName"));
                    String username = String.valueOf(rs.getString("Username"));
                    String email = String.valueOf(rs.getString("Email"));
                    String contactNo = String.valueOf(rs.getString("ContactNo"));
                    
                    String tbData[] = {userId,firstName,lastName,username,email,contactNo};
                    usersModel = (DefaultTableModel)tblUsers.getModel();
                    usersModel.addRow(tbData); 
                } 
                con.close();
                System.out.println("Database tables successfully loaded!");
            } catch (Exception e){
                System.out.println("Error " + e.getMessage());
                System.out.print("Loading of database tables unsuccessfull!");
            }
    }
    

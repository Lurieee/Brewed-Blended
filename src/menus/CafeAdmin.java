    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        String productName = tfProductName.getText();
        String productPrice = tfPrice.getText();
        String productInStock = tfInStock.getText();
        
        // If the product textfields are empty, show error; else, ask for confirmation to add product
        if (tfProductName.getText().isEmpty() || tfPrice.getText().isEmpty() || tfInStock.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Please fill in all the Product details.","No/Lacking Details Inputted",JOptionPane.ERROR_MESSAGE);
        } 
        else if (JOptionPane.showConfirmDialog(null, "Are you sure you want to add " +productName+ 
                " with the price of " +productPrice+ " to the database?", "Add New Product?",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try { 
                Class.forName("com.mysql.cj.jdbc.Driver");
                String url = "jdbc:MySQL://localhost:3306/brewedblended_db";
                String user = "root";
                String pass = "";
                Connection con = DriverManager.getConnection(url,user,pass);
                Statement st = con.createStatement();

                // Check if product exists within the database; if yes, show error; if not, then add new product with entered details
                String query;
                query = "SELECT * FROM products WHERE ProductName='"+productName+"';";
                ResultSet rs = st.executeQuery(query);
                if (rs.next()){
                    JOptionPane.showMessageDialog(this,"Product already exists within the database. \nPlease double-check the inputted product name.",
                        "Product Addition Unsuccessful",JOptionPane.ERROR_MESSAGE);
                    // Clear product textfields
                    clearProductTextFields();
                    System.out.print("Update of product unsuccessfull!");
                } else{
                    query = "INSERT INTO products (ProductName, Price, InStock) VALUES ('"+productName+"', "+productPrice+", "+productInStock+");";
                    st.executeUpdate(query);
                    // Update database tables in the program
                    loadDatabaseTables();
                    JOptionPane.showMessageDialog(this,productName + " successfully added to the database! Click the Load button below to view.",
                        "Product Addition Successful",JOptionPane.INFORMATION_MESSAGE);
                    // Clear product textfields
                    clearProductTextFields();
                    System.out.println("Product successfully added!");
                }
                con.close();
            } catch (HeadlessException | ClassNotFoundException | SQLException e){
                    System.out.println("Error " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAddProductActionPerformed


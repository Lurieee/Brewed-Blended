    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        // If Cart table is empty, show error; else, calculate order and print receipt
        if (tblCart.getRowCount() == 0){
            JOptionPane.showMessageDialog(this,"You have no item/s on your cart! \nPlease add product/s from the Menu on the right.","No Items on Cart",JOptionPane.ERROR_MESSAGE);
        } 
        else if (JOptionPane.showConfirmDialog(null, "Are you certain of your purchase? This action cannot be undone.", "Finalize Order?",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            areaReceipt.setText("=============================================\n");
            areaReceipt.setText(areaReceipt.getText()+"\t"+"-+ BREWED & BLENDED +-\n");
            areaReceipt.setText(areaReceipt.getText()+"=============================================\n");

            //Instantiate total items sold and total price
            int qtyTotal = 0;
            int qty;
            double priceTotal = 0;
            double price;

            //List down purchased items in Cart
            DefaultTableModel model = (DefaultTableModel)tblCart.getModel();
            areaReceipt.setText(areaReceipt.getText()+"QTY"+"\t"+"PRODUCT NAME"+"           \t"+"PRICE\n");
            for(int i = 0; i<tblCart.getRowCount(); i++){
                String productQty = tblCart.getValueAt(i,0).toString();
                String productName = tblCart.getValueAt(i,1).toString();
                String totalAmount = tblCart.getValueAt(i,2).toString();

                qty = Integer.parseInt(productQty);
                qtyTotal += qty; 
                price = Double.parseDouble(totalAmount);
                priceTotal += price; 
                areaReceipt.setText(areaReceipt.getText()+productQty+"\t"+productName+"   \t"+totalAmount+"\n");
                areaTotalPrice.setText("₱" + Double.toString(priceTotal));
                
                //Save to orders in the database
                saveOrderToDatabase(productName, productQty, totalAmount);
            }
            printReceiptDetails(Integer.toString(qtyTotal), Double.toString(priceTotal));
            writeToFile();
            JOptionPane.showMessageDialog(this,"Please present your receipt to the Brewed and Blended Cafe\n"
                    + " in Camachilli St, Corner Acacia in Matina within day of purchase!","Order Completed!",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBuyActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        // If Cart table is empty, show error; else, clear out the Cart table
        if (tblCart.getRowCount() == 0){
            JOptionPane.showMessageDialog(this,"You have no item/s in your cart! \nPlease add item/s from the Menu.","No Item/s in Cart",JOptionPane.ERROR_MESSAGE);
        }
        else if (JOptionPane.showConfirmDialog(null, "Are you sure you want to clear your Cart? This action cannot be undone.", 
                "Clear Cart?",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            // Clear the Receipt
            areaReceipt.setText("");
            // Clear the Cart
            DefaultTableModel model = (DefaultTableModel) tblCart.getModel();
            model.setRowCount(0);
            // Clear the Total Price
            areaTotalPrice.setText("");
            // Clear the textfile
            File receipt = new File("receipt.txt");
            FileWriter writer;
            try {
                //Clear receipt textfile
                writer = new FileWriter(receipt);
                writer.write("");
            } catch (IOException ex) {
                Logger.getLogger(CafeMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnClearActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblCart.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tblCart.getModel();
        
        // If Cart table is empty, show error; else if no row is selected, show error; else, delete selected row
        if (tblCart.getRowCount() == 0){
            JOptionPane.showMessageDialog(this,"You have no item/s in your cart! \nPlease add item/s from the Menu.","No Item/s in Cart",JOptionPane.ERROR_MESSAGE);
        }
        else if (row < 0) {
             JOptionPane.showMessageDialog(this,"Please select item from the table above.","No item selected!",JOptionPane.ERROR_MESSAGE);
        } 
        else if (JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the selected item? This action cannot be undone.", 
                "Delete Selected Item?",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            while(row != -1) {
                int rowModelId = tblCart.convertRowIndexToModel(tblCart.getSelectedRow());
                model.removeRow(rowModelId);
                break;
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


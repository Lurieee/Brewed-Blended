    private void saveOrderToDatabase(String productName, String productQty, String totalAmount){
        try { 
                Class.forName("com.mysql.cj.jdbc.Driver");
                String url = "jdbc:MySQL://localhost:3306/brewedblended_db";
                String user = "root";
                String pass = "";
                Connection con = DriverManager.getConnection(url,user,pass);
                Statement st = con.createStatement();
                String query;
                
                // Retrieve ProductId from the database
                query = "SELECT ProductId FROM products WHERE ProductName='"+productName+"';";
                ResultSet rs = st.executeQuery(query);
                if (rs.next()){
                    String productId = String.valueOf(rs.getInt("ProductId"));
                    // Retrieve username
                    String username = new UserRecord().getUsername();
                    // Retrieve UserId of the matching username from the database
                    query = "SELECT UserId FROM users WHERE Username='"+username+"';";
                    rs = st.executeQuery(query);
                    if (rs.next()){
                        String userId = String.valueOf(rs.getInt("UserId"));
                        
                        query = "INSERT INTO orders (UserId, ProductId, Quantity, TotalAmount, OrderDate) VALUES "
                                + "("+userId+", "+productId+", "+productQty+", "+totalAmount+", '"+getDate()+"');";
                        st.executeUpdate(query);
                    }
                } else{
                    System.out.println("Error saving order to database.");
                }
                con.close();
            } catch (HeadlessException | ClassNotFoundException | SQLException e){
                    System.out.println("Error " + e.getMessage());
            }
    }
    

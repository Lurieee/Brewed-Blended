    private int getProductDetails(int n) {
        // Arrays for component references
        JSpinner[] qtySpinners = {p1_Qty, p2_Qty, p3_Qty, p4_Qty, p5_Qty, p6_Qty, p7_Qty, p8_Qty, p9_Qty};
        JLabel[] nameLabels = {p1_Espresso, p2_CLatte, p3_HOCapuccino, p4_BWaffle, p5_CFCake, p6_FBrownies, p7_HChocolate, p8_SMilkshake, p9_MGTea};
        JLabel[] priceLabels = {p1_Price, p2_Price, p3_Price, p4_Price, p5_Price, p6_Price, p7_Price, p8_Price, p9_Price};

        // Ensure index n is valid (1-9)
        if (n < 1 || n > 9) {
            throw new IllegalArgumentException("Invalid product index: " + n);
        }
        // Get the components based on the index 
        JSpinner spnrQty = qtySpinners[n - 1];
        JLabel lblName = nameLabels[n - 1];
        JLabel lblPrice = priceLabels[n - 1];

        // Get product details and pass it to addToCart method
        int productQty = (int) spnrQty.getValue();
        if (productQty == 0) {
            JOptionPane.showMessageDialog(this, "Please select amount to buy.", "No quantity specified!", JOptionPane.ERROR_MESSAGE);
        } else {
            String productName = lblName.getText();
            double productPrice = Double.parseDouble(lblPrice.getText().substring(1)) * productQty;
            addToCart(productQty, productName, productPrice);
        }
        return 0;
    }

    private void addToCart(int productQty, String productName, double productPrice) {
        DefaultTableModel model = (DefaultTableModel) tblCart.getModel();
        
        // If Cart table is empty, show error; else, clear out the Cart table
        if (model.getRowCount() == 0){
            model.addRow(new Object[] {productQty, productName, productPrice});
        } else {
            //Check if item is already in Cart
            for(int r = 0; r < model.getRowCount();r++){
                // If it is, remove it then add again with new product details; else, add product to Cart
                if (productName.equals(model.getValueAt(r, 1))){
                    model.removeRow(r);
                    model.addRow(new Object[] {productQty, productName, productPrice});
                    break;
                } else if (r == (model.getRowCount()-1)){
                    model.addRow(new Object[] {productQty, productName, productPrice});
                    break;
                }
            }
        }
    }
    
